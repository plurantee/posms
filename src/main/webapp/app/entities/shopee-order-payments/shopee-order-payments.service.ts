import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IShopeeOrderPayments } from '@/shared/model/shopee-order-payments.model';

const baseApiUrl = 'api/shopee-order-payments';

export default class ShopeeOrderPaymentsService {
  public find(id: number): Promise<IShopeeOrderPayments> {
    return new Promise<IShopeeOrderPayments>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IShopeeOrderPayments): Promise<IShopeeOrderPayments> {
    return new Promise<IShopeeOrderPayments>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IShopeeOrderPayments): Promise<IShopeeOrderPayments> {
    return new Promise<IShopeeOrderPayments>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IShopeeOrderPayments): Promise<IShopeeOrderPayments> {
    return new Promise<IShopeeOrderPayments>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
